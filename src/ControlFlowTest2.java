public class ControlFlowTest2 {
    public static void main(String[] args) {
        int aNumber=3;
        switch (aNumber){
            case 1:
                System.out.println("First String");
                break;
            case 2:
                System.out.println("Second String");
                break;
            case 3:
                System.out.println("Third String");
                break;
        }
    }
}